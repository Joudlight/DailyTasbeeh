/* ============== Helper: flatten all adhkar for search ============== */
window.getAllAdhkar = function () {
  const all = [];
  for (const cat of window.ADHKAR_CATEGORIES) {
    const items = window.ADHKAR_DATA[cat.id] || [];
    items.forEach((item, idx) => {
      all.push({ ...item, categoryId: cat.id, categoryName: cat.name, order: idx });
    });
  }
  return all;
};

